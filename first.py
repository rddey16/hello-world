import numpy as np
import math
from scipy.stats import norm


#rho = 0.1
lam = .08
R = 0.4

def loss(t, tau):
    """ L(t) """ 
    count = sum(i <= t  for i in tau)
    l = count*(1-R)
    return(l)

def rec(t,tau):
    """ Recovery R(t) """
    count = sum(i <= t  for i in tau)
    r1 = R*count
    return(r1)
    
def trrec(t,tau,a,d):
    """  Tranche recovery """
    rr = rec(t,tau)
    rectr = min((d-a),max(rr+d-100,0))
    return(rectr)    

def trloss(t,tau,a,d):
    """ Tranche loss L(t)[a,d] """
    l = loss(t,tau)
    ltr = (max(l-a,0)-max(l-d,0))
    return(ltr)

def copula(rho):
    """ Generates 100 default times with Gauss Copula """
    tau=[]
    s = np.random.normal(0, 1, 101)
    for i in range(100):
        y = math.sqrt(rho)*s[0] + math.sqrt(1-rho)*s[i+1]
        u = norm.cdf(y)
        v = -np.log(1-u)/lam
        tau.append(v)
    return(tau)

def trspread(eloss,erec):
    """ Computes Tranche break-even spread """
    a,d = 0, 10
    dl = math.exp(-0.02*.25)*(eloss[0])
    pl = math.exp(-0.02*.25)*(d-a - eloss[0] - erec[0])*.25
    
    for j in range(1,20):
        dl = dl + math.exp(-0.02*(j+1)*.25)*(eloss[j] - eloss[j-1])
        pl = pl + math.exp(-0.02*(j+1)*.25)*(d-a - eloss[j] - erec[j])*.25
    
    spread = dl/pl
    return(spread)

def ELER(rho,a,d):
    """ Computes EL_(a,d), ER_(a,d) for qrtrly dates """
    el,er = [],[]
    for t in range(20):
        trl,trr = [],[]
        for k in range(100):
            tau = copula(rho)
            trr.append(trrec((t+1)*.25,tau,a,d))
            trl.append(trloss((t+1)*.25,tau,a,d))
        exploss, exprec =np.mean(trl), np.mean(trr)
        el.append(exploss)
        er.append(exprec)
    return(el,er)
    
